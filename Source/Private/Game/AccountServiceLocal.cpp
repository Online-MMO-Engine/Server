    SPtr<Account> AccountServiceLocal::Load(Ref<TOMLParser> Parser)
    {
        const TOMLSection Root = Parser.GetRoot();

        return NewPtr<Account>(Root.GetNumber("ID"), Root.GetString("Username"), Root.GetString("Password"));
    }

